package cursojava.executavel;

public class Exercicio01 {

	/* Main é um metodo auto executavel em Java */
	public static void main(String[] args) {

		/*Estrutura de Repetição while*/
		
		int numero = 0;
		
		while (numero <= 3) {
			System.out.println("O Numero atual é: " + numero);
			numero++;
		}
	}
}
